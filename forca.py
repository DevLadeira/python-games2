import random

def jogar():

    imprimir_mensagem_inicial()

    palavra_secreta = escolher_palavra()
    letras_acertadas = ["_" for letra in palavra_secreta]

    enforcou = False
    acertou = False
    erros = 0

    print(letras_acertadas)
        
    while (not acertou and not enforcou):
        chute = esperar_input()

        if chute in palavra_secreta:
            marcar_acertos(palavra_secreta, chute, letras_acertadas)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if(acertou):
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdedor(palavra_secreta)

#FIM DO JOGO

def imprimir_mensagem_inicial():
    print(40*"#")
    print("###### Bem-vindo ao jogo da Forca ######")
    print(40*"#")


def escolher_palavra(arquivo_palavras="palavras.txt", linha_inicial=0):
    palavras = []
    with open(arquivo_palavras, "r") as arquivo:
        for palavra in arquivo:
            palavras.append(palavra.strip())

    randindex = random.randrange(linha_inicial, len(palavras))

    return palavras[randindex].lower()


def esperar_input():
    chute = input("Tente uma letra: ")
    return chute.strip().lower()


def marcar_acertos(palavra_secreta, chute, letras_acertadas):
    index = 0
    for letra in palavra_secreta:
        if (chute == letra):
            letras_acertadas[index] = letra
        index += 1


